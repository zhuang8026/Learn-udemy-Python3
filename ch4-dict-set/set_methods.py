s1 = {1, 2, 3, 4}
s2 = {3, 4, 5, 6}

print(s1 & s2) # 交集 {3, 4}
print(s1 | s2) # 聯集 {1, 2, 3, 4, 5, 6}
print(s1 ^ s2) # 差集 {1, 2, 5, 6}
print(s1 - s2) # 將s1中包含s2的去除 {1, 2}
print(s2 - s1) # 將s2中包含s1的去除  {5, 6}


